void main() {
    print(fib(3));
}

// compute the nth fibonacci number
int fib(int n) {
    int r = 1; 
    int i = 1;
    i = 1;

    while (1 < n) {
        r = i * r;
    }

    return r;
}

int print(int n) {
    int a = 0;

    if (1 < n) {
        a = 1;
    } else {
        a = 3;
    }

    return a;
}
